// 按ID查找卡牌
export function getCardById(id: string): TarotCard | undefined {
  return TAROT_CARDS.find(c => c.id === id)
}

// 按类型筛选
export function getMajorArcana(): TarotCard[] {
  return TAROT_CARDS.filter(c => c.type === 'major')
}

// 按花色筛选
export function getSuit(suit: 'cups' | 'swords' | 'pentacles' | 'wands'): TarotCard[] {
  return TAROT_CARDS.filter(c => c.suit === suit)
}
